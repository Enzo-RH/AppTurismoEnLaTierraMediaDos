public class TipoAtraccion {
	
	private String nombre;

	public TipoAtraccion(String nombre) {
		this.nombre = nombre;
	}
	
	public TipoAtraccion() {
		super();
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
}
